					<h2>Le Class Tri... Qu'est ce que c'est?</h2>

					<p>Le Class Triathlon est un outil mis en place par la F.F.TRI. permettant d’évaluer le niveau des jeunes (benjamins, minimes, cadets, juniors) à partir de performances chronométriques réalisées en natation et en course à pied.</p>

					<p>Un classement national est ensuite établi à partir des points marqués sur ces épreuves chronométrées selon des tables de cotation spécifiques à la catégorie et au sexe (les 100 points sont basés sur le record national de la discipline, quelquefois pondérés, ou sur la meilleure performance nationale de l’année) Un barème adapté à chacune des catégories d’âges permet alors d’obtenir un classement national pour tous les jeunes ayant réalisés ces tests.</p>

					<ul>
						<li>Catégories Benjamin et Minime : Natation = 200m et Course à pied = 1000m</li>
						<li>Catégorie Cadet : Natation = 400m et Course à pied = 1500m</li>
						<li>Catégorie Junior : Natation = 400m et Course à pied = 3000m</li>
					</ul>

					<h2>Réglement</h2>

					<p>Le Class Triathlon est valide uniquement entre le 1er janvier et le 31 décembre, en correspondance avec la saison sportive. Si une partie de l’animation du Class Tri peut reposer sur l’organisation de sessions spécifiques, il est important qu’elle s’inscrive également dans une dynamique d’opportunités tout au long de l’année, pour autant que les conditions de validation des temps soient favorables. La réalisation des épreuves natation et course à pied peuvent être ainsi dissociées dans le temps sur la période définie précédemment.</p>
					<p>Les tests peuvent être organisés :</p>
					<ul>
						<li>- par les structures d’entraînement (clubs, pôles, centre régional d’entrainement, sections sportives scolaires) ;</li>
						<li>- par les comités départementaux ;</li>
						<li>- par les ligues régionales ;</li>
						<li>- par l’UNSS dans le cadre de partenariat avec la F.F.TRI ;</li>
						<li>- par ailleurs, peuvent être pris en compte les temps réalisés lors de compétitions de natation et d’athlétisme (FFA, FFN, UGSEL, FFSU, UNSS, voire de fédérations étrangères) à condition de faire l’objet d’un classement officiel vérifiable.</li>
					</ul>




					<p>A l’issue de la réalisation de sessions Class Triathlon ou de compétitions officielles en natation et en athlétisme, les temps sont enregistrés par les personnes disposant de l’autorisation nécessaire selon la catégorie et les distances associées dans <a href="https://espacetri.fftri.com">la base de données espace tri 2.0</a>. La saisie est alors effectuée, les temps sont dits « en attente de validation ».</p>


					<p>Les résultats publiés sur <a href="https://espacetri.fftri.com/classtri/national/list">l’espace tri</a> sont ceux validés par le Conseiller Technique de la Ligue (CTL) concernée ou par un Conseiller Technique National (CTN). Il appartient au CTL de vérifier les données inscrites dans la base avant de les valider. De leur côté, les CTN informent les CTL de l’inscription de résultats des athlètes licenciés dans un club relevant de leur ligue. Les CTN ont le droit d’invalider des résultats, après avoir demandé des explications auprès du C.T.L. de la ligue concernée. Les CTN et les CTL sont donc les garants de la légitimité du dispositif. En cas de litige, la décision ultime de prise en compte de résultats relève du Directeur Technique National.</p>
					
